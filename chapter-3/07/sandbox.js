const grade = 'E';

switch (grade) {
  case 'A':
    console.log('你的得分为A!');
    break;
  case 'B':
    console.log('你的得分为B!');
    break;
  case 'C':
    console.log('你的得分为C!');
    break;
  case 'D':
    console.log('你的得分为D!');
    break;
  case 'E':
    console.log('你的得分为E!');
    break;
  default:
    console.log('等分无效！');
}
